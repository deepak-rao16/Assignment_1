//Que2: Implement a JAVA program to check the default values of the Datatypes
class Data_type{
	static boolean num1;
	static int num2;
	static float num3;
	static String num4;
	static double num5;
	static long num6;
	
	public static void main(String[] args){
		System.out.println("Default values are: \n");
		System.out.println("Boolean_ : "+num1);
		System.out.println("Int_ : "+num2);
		System.out.println("Float_ : "+num3);
		System.out.println("String_ : "+num4);
		System.out.println("Double_ : "+num5);
		System.out.println("Long_ :"+num6);
	}
}	